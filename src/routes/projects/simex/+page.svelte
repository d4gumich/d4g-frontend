<h1>
    Digital Crisis Simulation Exercise (SimEx)
</h1>
<p>The digital Crisis Simulation Exercise (SimEx) is the latest iteration of a multi-year training lab
    designed to simulate disaster response strategies and actions within a realistic crisis setting. The
    COVID-19 Pandemic highlighted the need for remote simulation experiences to enable practicing
    team-based collaboration and critical thinking within a controlled, distanced environment.<br/><br/> The
    SimEx, built in Minecraft, places users directly in the heart of a war-torn country to recreate the
    stress and urgency inherent in disaster response situations. Users must effectively demonstrate
    situational awareness, communication, and devise a successful plan to safely escape the conflict and
    evacuate the team across an international border.
</p>
